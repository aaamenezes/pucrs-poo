package dados;

public class Jogo {
    private int codigo;
    private String nome;
    private int ano;
    private double valorMinuto;

    public Jogo(int codigo, String nome, int ano, double valorMinuto) {
        this.codigo = codigo;
        this.nome = nome;
        this.ano = ano;
        this.valorMinuto = valorMinuto;
    }
}
