package dados;

public class Jogo {
    private int id;
    private String name;
    private int year;
    private double valuePerMinute;

    public Jogo(int id, String name, int year, double valuePerMinute) {
        this.id = id;
        this.name = name;
        this.year = year;
        this.valuePerMinute = valuePerMinute;
    }
}
